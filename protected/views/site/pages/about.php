<h1>About MyChoralRep</h1>

<p>Before the MyChoralRep, there was no web presence of sharing choral programs with other peers. The only way that this could be accomplished was through emails to a specific person. There was no one centralized choral program site. With MyChoralRep, people can contact and distribute programs easily as well as notice trends among programs with what repertoire is being performed. For example, choral directors can see what is being played the most across the country in a specific grade level.</p>
